#!/bin/bash

echo "enter a number"
read num
if [  $num -eq 0 ]
	then
	echo "0:zero"
elif [ $num -eq 1 ]
	then
	echo "1:One"
elif [ $num -eq 2 ]
	then
	echo "2:Two"
elif [ $num -eq 3 ]
	then
	echo "3:Three"
elif [ $num -eq 4 ]
	then
	echo "4;four"
elif [ $num -eq 5 ]
        then
        echo "5:fiv"
elif [ $num -eq 6 ]
        then
        echo "6:six"
elif [ $num -eq 7 ]
        then
        echo "7:seven"
elif [ $num -eq 8 ]
        then
        echo "8:eight"
else
   echo "nine"




fi
